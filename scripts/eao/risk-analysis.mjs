#!/usr/bin/env node
// Executable Risk Analysis Pipeline.
// See brain/AI/EAO_EXECUTION_PIPELINES.md #8.
//
// Foundation-first note: no new repository scanning, no second source of
// truth, no duplicated business logic. The canonical action model produced
// by computeProjectHealth() (via buildPriorityActions()) is this pipeline's
// only primary input - every risk below is a direct restatement of an
// already-computed action's fields, never re-derived from priority numbers
// or re-inferred severity.
//
// Read-only.

import { pathToFileURL } from "node:url";
import { computeProjectHealth } from "./project-health.mjs";

// Risk Level is derived only from canonical fields already present on each
// action (severity, governanceSensitive, affectsArchitecture) - never from
// the action's priority number, which is an ordering hint, not a severity signal.
function classifyRiskLevel(action) {
  if (action.severity === "critical" && (action.governanceSensitive || action.affectsArchitecture)) {
    return "Critical";
  }
  if (action.severity === "critical") return "High";
  return "Moderate";
}

// Mitigation recommendation derived only from canonical fields (autoFixable,
// humanApprovalRequired) - not a new judgment invented per risk.
function mitigationFor(action) {
  const approval = "Requires human approval before any change is applied (EAO_PERMISSION_MODEL.md).";
  if (action.autoFixable) {
    return `Mechanical fix identifiable from the evidence itself (e.g. known replacement text or deterministic reformatting). ${approval}`;
  }
  return `Requires human investigation to determine the correct resolution - no deterministic fix is derivable from the evidence alone. ${approval}`;
}

export function computeRiskAnalysis(root = process.cwd()) {
  const health = computeProjectHealth(root);

  const riskRegister = health.priorityActions.map((action, idx) => ({
    riskId: `RISK-${String(idx + 1).padStart(2, "0")}`,
    title: action.action,
    riskDomain: action.riskDomain,
    category: action.category,
    riskLevel: classifyRiskLevel(action),
    count: action.count,
    governanceSensitive: action.governanceSensitive,
    affectsArchitecture: action.affectsArchitecture,
    autoFixable: action.autoFixable,
    humanApprovalRequired: action.humanApprovalRequired,
    sourcePipeline: action.sourcePipeline,
    mitigation: mitigationFor(action),
    evidence: action.evidence,
  }));

  const byDomain = new Map();
  for (const r of riskRegister) {
    if (!byDomain.has(r.riskDomain)) byDomain.set(r.riskDomain, []);
    byDomain.get(r.riskDomain).push(r);
  }

  // Risk Matrix: Impact (from severity, via riskLevel) x Remediation Effort
  // (from autoFixable) - both dimensions read directly from canonical fields,
  // no new severity model invented.
  const matrix = {
    "Critical / Low-effort": riskRegister.filter((r) => r.riskLevel === "Critical" && r.autoFixable).length,
    "Critical / Higher-effort": riskRegister.filter((r) => r.riskLevel === "Critical" && !r.autoFixable).length,
    "High / Low-effort": riskRegister.filter((r) => r.riskLevel === "High" && r.autoFixable).length,
    "High / Higher-effort": riskRegister.filter((r) => r.riskLevel === "High" && !r.autoFixable).length,
    "Moderate / Low-effort": riskRegister.filter((r) => r.riskLevel === "Moderate" && r.autoFixable).length,
    "Moderate / Higher-effort": riskRegister.filter((r) => r.riskLevel === "Moderate" && !r.autoFixable).length,
  };

  const executiveSummary = {
    overallStatus: health.overallStatus,
    totalRisks: riskRegister.length,
    byLevel: {
      Critical: riskRegister.filter((r) => r.riskLevel === "Critical").length,
      High: riskRegister.filter((r) => r.riskLevel === "High").length,
      Moderate: riskRegister.filter((r) => r.riskLevel === "Moderate").length,
    },
    byDomain: Object.fromEntries([...byDomain.entries()].map(([d, rs]) => [d, rs.length])),
  };

  return { schemaVersion: health.schemaVersion, generatedAt: health.generatedAt, executiveSummary, riskRegister, byDomain, matrix };
}

export function renderRiskAnalysisMarkdown(result) {
  const { executiveSummary, riskRegister, byDomain, matrix } = result;
  const lines = [];
  lines.push("## Risk Analysis Report");
  lines.push("");
  lines.push(
    "_Generated by scripts/eao/risk-analysis.mjs - composes computeProjectHealth()'s canonical action model as its only primary input. No additional repository scanning performed._"
  );
  lines.push("");

  lines.push("### Executive Risk Summary");
  lines.push("");
  lines.push(
    `Overall status: **${executiveSummary.overallStatus}**. ${executiveSummary.totalRisks} risk(s) tracked: ` +
      `${executiveSummary.byLevel.Critical} Critical, ${executiveSummary.byLevel.High} High, ${executiveSummary.byLevel.Moderate} Moderate.`
  );
  lines.push("");
  lines.push(
    `By domain: ${Object.entries(executiveSummary.byDomain).map(([d, c]) => `${d} (${c})`).join(", ") || "none"}.`
  );
  lines.push("");

  lines.push("### Risk Register");
  lines.push("");
  lines.push("| ID | Risk | Domain | Level | Count | Governance-Sensitive | Auto-Fixable | Source Pipeline |");
  lines.push("|---|---|---|---|---|---|---|---|");
  for (const r of riskRegister) {
    lines.push(
      `| ${r.riskId} | ${r.title} | ${r.riskDomain} | ${r.riskLevel} | ${r.count} | ${r.governanceSensitive} | ${r.autoFixable} | ${r.sourcePipeline} |`
    );
  }
  if (!riskRegister.length) lines.push("| _none_ | | | | | | | |");
  lines.push("");

  lines.push("### Risk Grouped by Domain");
  lines.push("");
  for (const [domain, risks] of byDomain.entries()) {
    lines.push(`**${domain} (${risks.length}):**`);
    lines.push(risks.map((r) => `- ${r.riskId}: ${r.title} (${r.riskLevel})`).join("\n"));
    lines.push("");
  }

  lines.push("### Risk Matrix (Impact x Remediation Effort)");
  lines.push("");
  lines.push("| Impact | Low-effort | Higher-effort |");
  lines.push("|---|---|---|");
  lines.push(`| Critical | ${matrix["Critical / Low-effort"]} | ${matrix["Critical / Higher-effort"]} |`);
  lines.push(`| High | ${matrix["High / Low-effort"]} | ${matrix["High / Higher-effort"]} |`);
  lines.push(`| Moderate | ${matrix["Moderate / Low-effort"]} | ${matrix["Moderate / Higher-effort"]} |`);
  lines.push("");
  lines.push(
    "_Impact derived from severity + governance/architecture sensitivity (canonical fields). " +
      "Remediation Effort derived from autoFixable (canonical field). Neither dimension is fabricated - both trace directly to Project Health's action model._"
  );
  lines.push("");

  lines.push("### Mitigation Recommendations");
  lines.push("");
  for (const r of riskRegister) {
    lines.push(`- **${r.riskId} (${r.title}):** ${r.mitigation}`);
  }
  if (!riskRegister.length) lines.push("- none");
  lines.push("");

  lines.push("### Human Approval Required");
  lines.push("");
  lines.push(
    "_Every risk below requires explicit human approval before any file modification, per EAO_PERMISSION_MODEL.md - no exceptions, no auto-applied fixes regardless of the autoFixable flag._"
  );
  lines.push(riskRegister.length ? riskRegister.map((r) => `- ${r.riskId}: ${r.title}`).join("\n") : "- none");
  lines.push("");

  lines.push("### Evidence References");
  lines.push("");
  for (const r of riskRegister) {
    // Task 4 (Phase A): evidence is now always an array (project-health.mjs
    // normalizes it at the source) - no shape-detection workaround needed.
    const evCount = r.evidence.length;
    lines.push(`- ${r.riskId}: ${evCount} evidence record(s) (see Project Health / originating pipeline output for full detail)`);
  }
  if (!riskRegister.length) lines.push("- none");

  return lines.join("\n");
}

const isMain = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;
if (isMain) {
  console.log(renderRiskAnalysisMarkdown(computeRiskAnalysis()));
}
