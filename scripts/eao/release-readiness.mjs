#!/usr/bin/env node
// Executable Release Readiness Pipeline.
// See brain/AI/EAO_EXECUTION_PIPELINES.md #9.
//
// Foundation-first note: no new repository scanning, no second source of
// truth. computeProjectHealth() is the only primary input - Release Gates
// are evaluated directly against its canonical priorityActions (by category),
// and the MVP-implementation signal comes from releaseReadinessHealth, which
// was itself added at the correct source (dependency-map.mjs's
// mvpStatusFindings, propagated through Project Health) rather than
// reconstructed here.
//
// Read-only.

import { pathToFileURL } from "node:url";
import { computeProjectHealth } from "./project-health.mjs";
import { CATEGORIES } from "./lib/registry.mjs";

// Each gate maps to a specific canonical action category already computed by
// Project Health - no new detection logic, just a named pass/fail lens on
// data that already exists.
const GATE_DEFINITIONS = [
  { name: "No unresolved terminology drift", categories: [CATEGORIES.TERMINOLOGY_DRIFT] },
  { name: "No governance document connectivity issues", categories: [CATEGORIES.GOVERNANCE_CONNECTIVITY] },
  { name: "No broken documentation references or links", categories: [CATEGORIES.BROKEN_REFERENCE, CATEGORIES.BROKEN_LINK] },
  { name: "All core documents referenced", categories: [CATEGORIES.UNREFERENCED_CORE_DOCUMENT] },
  { name: "MVP-critical specifications confirmed implemented", categories: [CATEGORIES.MVP_IMPLEMENTATION_PENDING] },
];

export function computeReleaseReadiness(root = process.cwd()) {
  const health = computeProjectHealth(root);

  const gates = GATE_DEFINITIONS.map((g) => {
    const matchingActions = health.priorityActions.filter((a) => g.categories.includes(a.category));
    const status = matchingActions.length ? "Fail" : "Pass";
    const blocking = matchingActions.some((a) => a.severity === "critical");
    return { gate: g.name, status, blocking, evidence: matchingActions };
  });

  const blockingIssues = health.priorityActions.filter((a) => a.severity === "critical");
  const nonBlockingIssues = health.priorityActions.filter((a) => a.severity === "warning");

  const gatesPassed = gates.filter((g) => g.status === "Pass").length;
  const releaseReadinessScore = gates.length ? Math.round((gatesPassed / gates.length) * 100) : 100;

  const anyBlockingGateFailed = gates.some((g) => g.status === "Fail" && g.blocking);
  const recommendedDecision = anyBlockingGateFailed ? "No-Go" : "Go";

  const executiveSummary = {
    overallProjectStatus: health.overallStatus,
    releaseReadinessScore,
    gatesPassed,
    totalGates: gates.length,
    blockingIssueCount: blockingIssues.length,
    nonBlockingIssueCount: nonBlockingIssues.length,
    recommendedDecision,
  };

  return {
    schemaVersion: health.schemaVersion,
    executiveSummary,
    gates,
    blockingIssues,
    nonBlockingIssues,
    remainingTechnicalDebt: health.priorityActions.filter((a) => a.category === CATEGORIES.TECHNICAL_DEBT),
    recommendedDecision,
  };
}

export function renderReleaseReadinessMarkdown(result) {
  const { executiveSummary: s, gates, blockingIssues, nonBlockingIssues, remainingTechnicalDebt, recommendedDecision } = result;
  const lines = [];
  lines.push("## Release Readiness Report");
  lines.push("");
  lines.push(
    "_Generated by scripts/eao/release-readiness.mjs - composes computeProjectHealth()'s canonical action model and releaseReadinessHealth as its only inputs. No additional repository scanning performed._"
  );
  lines.push("");

  lines.push("### Executive Release Readiness Summary");
  lines.push("");
  lines.push(
    `Overall project status: **${s.overallProjectStatus}**. Release Readiness Score: **${s.releaseReadinessScore}%** (${s.gatesPassed}/${s.totalGates} gates passed). ` +
      `${s.blockingIssueCount} blocking issue(s), ${s.nonBlockingIssueCount} non-blocking issue(s). Recommended decision: **${s.recommendedDecision}**.`
  );
  lines.push("");

  lines.push("### Release Readiness Score");
  lines.push("");
  lines.push(`**${s.releaseReadinessScore}%** — derived as (gates passed / total gates) x 100, from the Release Gates below. Not a fabricated metric - a direct ratio of already-computed pass/fail gates.`);
  lines.push("");

  lines.push("### Release Gates");
  lines.push("");
  lines.push("| Gate | Status | Blocking | Evidence Count |");
  lines.push("|---|---|---|---|");
  for (const g of gates) {
    lines.push(`| ${g.gate} | ${g.status} | ${g.blocking} | ${g.evidence.length} |`);
  }
  lines.push("");

  lines.push(`### Blocking Issues — ${blockingIssues.length}`);
  lines.push("");
  lines.push(
    blockingIssues.length
      ? blockingIssues.map((a) => `- ${a.action} (${a.count}) — category: ${a.category}`).join("\n")
      : "- none"
  );
  lines.push("");

  lines.push(`### Non-blocking Issues — ${nonBlockingIssues.length}`);
  lines.push("");
  lines.push(
    nonBlockingIssues.length
      ? nonBlockingIssues.map((a) => `- ${a.action} (${a.count}) — category: ${a.category}`).join("\n")
      : "- none"
  );
  lines.push("");

  lines.push(`### Remaining Technical Debt — ${remainingTechnicalDebt.length}`);
  lines.push("");
  lines.push(
    remainingTechnicalDebt.length
      ? remainingTechnicalDebt.map((a) => `- ${a.action} (${a.count})`).join("\n")
      : "- none"
  );
  lines.push("");

  lines.push("### Human Approval Gates");
  lines.push("");
  lines.push(
    "_This pipeline only recommends a Go/No-Go decision - the actual release decision, and any fix applied before release, requires explicit human approval (EAO_PERMISSION_MODEL.md). No gate here is self-executing._"
  );
  lines.push("");

  lines.push("### Recommended Release Decision");
  lines.push("");
  lines.push(
    `**${recommendedDecision}**${recommendedDecision === "No-Go" ? " — one or more blocking gates failed (see Release Gates above)." : " — no blocking gate failed."}`
  );
  lines.push("");

  lines.push("### Evidence References");
  lines.push("");
  for (const g of gates) {
    lines.push(`- ${g.gate}: ${g.evidence.length} evidence record(s) (see Project Health's priorityActions for full detail)`);
  }
  lines.push("");

  lines.push("### Go / No-Go Summary");
  lines.push("");
  lines.push(
    recommendedDecision === "Go"
      ? "**GO** — all blocking release gates pass. Non-blocking issues and technical debt remain but do not block release."
      : `**NO-GO** — ${gates.filter((g) => g.status === "Fail" && g.blocking).length} blocking gate(s) failed. Resolve blocking issues (or obtain explicit human override) before release.`
  );

  return lines.join("\n");
}

const isMain = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;
if (isMain) {
  console.log(renderReleaseReadinessMarkdown(computeReleaseReadiness()));
}
